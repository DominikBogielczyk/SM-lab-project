/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

  void update_display()
  {
	lcd16x2_clear();
	lcd16x2_setCursor(0, 0);

	switch(display_mode)
	{
		case 0:
			lcd16x2_printf("Temperature");

			lcd16x2_setCursor(1, 8);
			lcd16x2_printf("%.2f %cC", temperature, 223); //223 - celsius grad symbol
			break;

		case 1:
			lcd16x2_printf("Setpoint");

			lcd16x2_setCursor(1, 8);
			lcd16x2_printf("%.2f %cC", setpoint, 223);
			break;

		case 2:
			lcd16x2_printf("Heating");

			lcd16x2_setCursor(1, 12);
			char text[2];
			sprintf(text, "%d", duty/10);
			lcd16x2_printf(text);

			lcd16x2_setCursor(1, 15);
			lcd16x2_printf("%c", 37); //% sign - 37 in ASCII table
			break;
	}
  }

  BMP280_Init(&hi2c1, 1, 3, 1); //temperature sensor
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1); //control signal
  HAL_TIM_Base_Start_IT(&htim4); //fixed sampling time Tp = 1sec

  lcd16x2_init_4bits(LCD_RS_GPIO_Port, LCD_RS_Pin, LCD_E_Pin,
        LCD_D4_GPIO_Port, LCD_D4_Pin, LCD_D5_Pin, LCD_D6_Pin, LCD_D7_Pin);

  lcd16x2_cursorShow(0);
  update_display();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	temperature = BMP280_ReadTemperature();

	if((fabs(prev_temperature - temperature) > 0.05) && display_mode == 0)
	{
		 update_display();
		 prev_temperature = temperature;
	}
	else if(prev_heating != (duty/10))
	{
		update_display();
		prev_heating = duty/10;
	}

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

